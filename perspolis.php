            <p>Perspolis was the ceremonial capital of the <a href="https://en.wikipedia.org/wiki/Achaemenid_Empire" target="_blank">Achaemenid Empire </a> (ca. 550–330 BC). The Achaemenid Empire, founded by <a href="https://en.wikipedia.org/wiki/Cyrus_the_Great" target="_blank">Cyrus the Great</a> , was the first true global superpower state and it ruled from the Balkans to North Africa and also Central Asia, spanning three continents, from their seat of power in Persis (<a href="perspolis.php">Persepolis</a>). It was the largest empire yet seen and the first world empire. The Achaemenid Empire was the only civilization in all of history to connect over 40% of the global population, accounting for approximately 49.4 million of the world's 112.4 million people in around 480 BC. They were succeeded by the Seleucid, Parthian, and Sasanian Empires, who successively governed Iran for almost 1,000 years and made Iran once again as a leading power in the world. Persia's arch-rival was the Roman Empire and its successor, the Byzantine Empire.</p>
        
